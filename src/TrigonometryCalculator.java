public class TrigonometryCalculator {

    public double sinA(double a){
        return Math.sin(a);
    }

    public double cosA(double a){
        return Math.cos(a);
    }

    public double tgA(double a) { return Math.tan(a);    }

    public double ctgA(double a) {
        return 1.0/Math.tan(a);
    }

}
